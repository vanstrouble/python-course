# Delving into floating number systems
a = 3.0
# print(f'a: {a:.2f}')

# Float constructor
a = float(10)
# print(f'a: {a:.2f}')

# Exponential notation
a = 3e5  # Positive
# print(f'a: {a}')

a = 3e-5  # Negative
# print(f'a: {a:.5f}')

# Any calculation involving float is promoted to float
a = 4.0 + 5
# print(f'a: {a}')

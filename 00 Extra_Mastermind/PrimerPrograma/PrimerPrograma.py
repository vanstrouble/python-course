# Reto de detectar los valores menores y mayores

n1 = int(input("Digita el primer número: "))
n2 = int(input("Digita el segundo número: "))
n3 = int(input("Digita el tercer número: "))

print(f"El número más grande entre {n1}, {n2} y {n3} es: {max(n1, n2, n3)}")
print(f"El número más pequeño entre {n1}, {n2} y {n3} es: {min(n1, n2, n3)}")
